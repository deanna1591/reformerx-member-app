export type TranslationKey = keyof (typeof dict)["en"];

/** Locale for this request: cookie first, then the browser's Accept-Language. */
export function getLocale(): Locale {
  const cookie = cookies().get("rx_lang")?.value;
  if (cookie === "cs" || cookie === "en") return cookie;
  try {
    const accept = headers().get("accept-language") ?? "";
    if (/(^|,|\s)cs\b/i.test(accept)) return "cs";
  } catch {
    /* headers unavailable in some contexts */
  }
  return "en";
}

/** Translate, with {placeholder} substitution. Falls back to English. */
export function translate(locale: Locale, key: TranslationKey, params?: Record<string, string | number>): string {
  const table = dict[locale] as Record<string, string>;
  const fallback = dict.en as Record<string, string>;
  let out = table[key] ?? fallback[key] ?? String(key);
  if (params) {
    for (const [k, v] of Object.entries(params)) out = out.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  }
  return out;
}

/** Convenience: `const t = getT();` then `t("home.greeting", { name })`. */
export function getT() {
  const locale = getLocale();
  return (key: TranslationKey, params?: Record<string, string | number>) => translate(locale, key, params);
}

/** Intl locale string for dates and numbers. */
export function intlLocale(locale: Locale): string {
  return locale === "cs" ? "cs-CZ" : "en-GB";
}

/** Czech plurals for counts: 1 lekce, 2–4 lekce, 5+ lekcí. */
export function pluralClasses(locale: Locale, n: number): string {
  if (locale === "en") return n === 1 ? "class" : "classes";
  if (n === 1) return "lekce";
  if (n >= 2 && n <= 4) return "lekce";
  return "lekcí";
}

export function pluralDays(locale: Locale, n: number): string {
  if (locale === "en") return n === 1 ? "day" : "days";
  if (n === 1) return "den";
  if (n >= 2 && n <= 4) return "dny";
  return "dní";
}
